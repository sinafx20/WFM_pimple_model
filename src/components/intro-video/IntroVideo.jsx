const qs = () =>
  typeof window === "undefined" ? new URLSearchParams() : new URLSearchParams(window.location.search);

const param = (...names) => {
  const q = qs();
  for (const n of names) {
    const v = (q.get(n) || "").trim();
    if (v) return v;
  }
  return "";
};

const detectVertical = () => {
  const raw = param("industry", "vertical").toLowerCase();
  if (!raw) return VERTICALS[0];
  return (
    VERTICALS.find((v) => v.id === raw || v.label.toLowerCase() === raw || v.label.toLowerCase().includes(raw)) ||
    VERTICALS[0]
  );
};

/* Resolve the personalised video. Clay drops a HeyGen share link onto the
   contact and the email passes it as ?video=… (a direct .mp4 also works). */
const resolveVideo = () => {
  const raw = param("video", "video_url", "heygen");
  const thumb = param("thumb", "thumbnail");
  if (!raw) return { kind: "none" };
  if (/\.(mp4|webm|mov|m4v)(\?|$)/i.test(raw)) return { kind: "file", src: raw, thumb };
  // HeyGen share pages must be iframed via their /embeds/ URL, not /share/.
  const src = /heygen\.com\/share\//i.test(raw) ? raw.replace("/share/", "/embeds/") : raw;
  return { kind: "iframe", src, thumb };
};

/* Forward to the TP1 Health Check in the same deployed build, carrying identity
   so completion is recorded with zero friction and the right vertical preloads. */
const goToHealthCheck = () => {
  if (typeof window === "undefined") return;
  const q = qs();
  const next = new URLSearchParams({ tool: "tp1" });
  if (q.get("industry")) next.set("industry", q.get("industry"));
  if (q.get("email")) next.set("email", q.get("email"));
  window.location.href = `${window.location.pathname}?${next.toString()}`;
};

const goTo = (url) => {
  if (typeof window !== "undefined" && url && url !== "#") window.open(url, "_blank", "noopener,noreferrer");
};

